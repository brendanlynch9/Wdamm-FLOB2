import numpy as np
import math
from scipy.optimize import curve_fit
import matplotlib.pyplot as plt

# -----------------------------
# 1) Chunked von Mangoldt computation (memory efficient)
# -----------------------------
def von_mangoldt_chunked(N_chunk):
    """
    Returns von Mangoldt array for a chunk of length N_chunk
    """
    Lambda = np.zeros(N_chunk+1, dtype=float)
    is_prime = np.ones(N_chunk+1, dtype=bool)
    is_prime[:2] = False
    for p in range(2, int(math.sqrt(N_chunk))+1):
        if is_prime[p]:
            is_prime[p*p:N_chunk+1:p] = False
    primes = np.nonzero(is_prime)[0]
    for p in primes:
        pk = p
        while pk <= N_chunk:
            Lambda[pk] = math.log(p)
            if pk > N_chunk // p: break
            pk *= p
    return Lambda

# -----------------------------
# 2) Hardy-Littlewood singular series
# -----------------------------
def singular_series(h):
    C2 = 0.660161815846869
    factors = set()
    n, d = h, 2
    while d*d <= n:
        if n % d == 0:
            if d > 2: factors.add(d)
            while n % d == 0: n //= d
        d += 1
    if n > 2: factors.add(n)
    prod = 1.0
    for p in factors: prod *= (p-1)/(p-2)
    return 2*C2*prod

# -----------------------------
# 3) Linear inverse model for extrapolation
# -----------------------------
def linear_inverse(x_inv, B_inf, alpha):
    return B_inf + alpha * x_inv

# -----------------------------
# 4) Main streaming simulator
# -----------------------------
def polignac_streaming_infinity(gaps, N_max=int(1e10), chunk_size=int(5e6), num_points=50):
    print(f"Running Polignac Simulator up to N={N_max} with chunk size={chunk_size}")
    max_gap = max(gaps)
    
    # Prepare cumulative sums and positions
    S_cum = {h:0.0 for h in gaps}
    B_track = {h:[] for h in gaps}
    x_track = []
    
    # Points to track for extrapolation
    x_points = set(np.linspace(chunk_size, N_max, num_points, dtype=int))
    
    for start in range(1, N_max+1, chunk_size):
        end = min(start + chunk_size + max_gap, N_max + 1)
        Lambda = von_mangoldt_chunked(end - start)
        
        # Compute correlations within this chunk
        for h in gaps:
            # Ensure we stay within bounds
            length = end - start - h
            if length <= 0: continue
            S_cum[h] += np.sum(Lambda[0:length] * Lambda[h:h+length])
        
        # Track B values at requested x_points
        for x in x_points:
            if start <= x < start + chunk_size:
                x_track.append(x)
                for h in gaps:
                    B_values = S_cum[h] / (singular_series(h) * x)
                    B_track[h].append(B_values)
        
        print(f"Processed up to {end-1:,}")

    # -----------------------------
    # 5) Extrapolation
    # -----------------------------
    extrapolated_B = {}
    plt.figure(figsize=(14,9))
    
    for h in gaps:
        B_vals = np.array(B_track[h])
        x_vals = np.array(x_track)
        x_inv = 1.0 / x_vals
        popt, _ = curve_fit(linear_inverse, x_inv, B_vals)
        B_inf, alpha = popt
        extrapolated_B[h] = B_inf
        
        flux = np.mean((B_vals - linear_inverse(x_inv, *popt))**2)
        
        print(f"\nGap h={h}: Final B(N_max) = {B_vals[-1]:.6f}, Flux = {flux:.2e}, Extrapolated B_inf = {B_inf:.6f}")
        
        plt.plot(x_vals, B_vals, 'o', label=f'h={h} B_N={B_vals[-1]:.6f}')
        plt.plot(x_vals, linear_inverse(x_inv, *popt), '--', alpha=0.6)

    plt.axhline(1.0, color='r', linestyle='--', alpha=0.5, label='Hardy-Littlewood Limit')
    plt.xlabel('x', fontsize=12)
    plt.ylabel('Clustering Ratio B(h,x)', fontsize=12)
    plt.title(f'Polignac Gaps: B(h,x) Extrapolation to Infinity (N={N_max})', fontsize=14)
    plt.legend()
    plt.grid(True, linestyle='--', alpha=0.5)
    plt.show()

    return extrapolated_B

# -----------------------------
# 6) Execute
# -----------------------------
